import mongoose from "mongoose";

// This is kind like the user object, which also add constrains on the object. 
// userResp + Validation in spring boot
const userSchema = new mongoose.Schema({
    username: {
        type: String,
        required: true,
        unique: true
    },
    email: {
        type: String,
        required: true,
        unique: true
    },
    password: {
        type: String,
        required: true
    },
    profilePicture:{
        type: String,
        default: "https://media.istockphoto.com/id/1393750072/vector/flat-white-icon-man-for-web-design-silhouette-flat-illustration-vector-illustration-stock.jpg?s=612x612&w=0&k=20&c=s9hO4SpyvrDIfELozPpiB_WtzQV9KhoMUP9R9gVohoU=",
    },
    isAdmin: {
        type: Boolean,
        default: false,
    }
}, {timestamps: true});

const User = mongoose.model('User', userSchema);

export default User;